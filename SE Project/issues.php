            Like everyone else, transgender persons require both acute care when they are ill or injured as well as preventive care to be healthy. For some people, gender dysphoria may also require medical attention. Health treatment for transgender people is subject to widespread stigma and misunderstanding. Transition-related care could be portrayed as "cosmetic" or a personal preference by others who are unaware of the health requirements of transgender people. The truth is that medical professionals overwhelmingly agree that transition-related care is effective for many transgender people and is therefore required by law.
        </p>
