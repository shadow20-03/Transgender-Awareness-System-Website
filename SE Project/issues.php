            For many transgender people, discrimination is a daily occurrence that can have an impact in almost every aspect of life. According to the National Transgender Discrimination Survey, 20% of trans people were denied housing or evicted from their homes, 50% experienced workplace harassment, and 78% of trans students reported being harassed or physically assaulted. And when the trans person is a person of colour and additionally encounters aggravating racism, the transphobia that motivates the prejudice is intensified. The rate of prejudice against trans people of colour is higher.
